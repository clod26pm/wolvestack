def cleanup_file(path, fixes):
    try:
        html = path.read_text(encoding='utf-8', errors='ignore')
    except Exception:
        return False, 0
    orig = html
    repls = 0
    for pat, repl in fixes:
        if pat in html:
            n = html.count(pat)
            html = html.replace(pat, repl)
            repls += n
    if html != orig:
        path.write_text(html, encoding='utf-8')
        return True, repls
    return False, 0


def main():
    # Step 1: Cornerstone replacement (top 8 compounds × 5 aspects)
    cornerstone_compounds = ['bpc-157', 'tb-500', 'semaglutide', 'tirzepatide',
                             'retatrutide', 'ipamorelin', 'mk-677', 'cjc-1295']
    cornerstone_aspects = ['guide', 'faq', 'dosage', 'safety', 'research']
    targets = []
    for ck in cornerstone_compounds:
        if ck not in v2.COMPOUNDS:
            continue
        for aspect in cornerstone_aspects:
            if aspect == 'guide':
                targets.append(f'{ck}-guide.html')
            else:
                targets.append(f'{ck}-{aspect}.html')

    fixed = 0
    for t in targets:
        path = AR_DIR / t
        if not path.exists():
            continue
        try:
            if v2.process_file(path):
                fixed += 1
        except Exception as e:
            print(f"ERR {t}: {e}", file=sys.stderr)
    print(f"Step 1 (cornerstone): {fixed}/{len(targets)} files replaced with Arabic v2")

    # Step 2: Argos cleanup pass for ALL ar files
    cleanup_files = 0
    cleanup_repls = 0
    for path in sorted(AR_DIR.glob('*.html')):
        changed, n = cleanup_file(path, AR_CLEANUP)
        if changed:
            cleanup_files += 1
            cleanup_repls += n
    print(f"Step 2 (Argos cleanup): {cleanup_files} files modified, {cleanup_repls} replacements")


if __name__ == '__main__':
    main()
