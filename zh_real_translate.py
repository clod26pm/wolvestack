# Process zh files
# ============================================================
def process_file(zh_path, en_path):
    html = zh_path.read_text(encoding='utf-8')

    # Extract original title (Chinese) - keep it
    m_title = re.search(r'<title>(.*?)</title>', html, re.DOTALL)
    title_zh = m_title.group(1).strip() if m_title else zh_path.stem

    # Extract original meta description (Chinese quick answer)
    m_desc = re.search(r'<meta\s+content="([^"]*)"\s+name="description"', html)
    if not m_desc:
        m_desc = re.search(r'<meta\s+name="description"\s+content="([^"]*)"', html)
    quick_answer_zh = m_desc.group(1).strip() if m_desc else ''

    # Build new article body
    slug = zh_path.stem
    new_sections = build_article_body(slug, title_zh, quick_answer_zh)

    # Find <article ...>...</article> and replace its inner content
    pattern = re.compile(r'(<article[^>]*>)(.*?)(</article>)', re.DOTALL)
    if not pattern.search(html):
        # Some files may not have <article>; try replacing between hero and callout
        # Look for the body content area after the hero
        return False

    new_article = f'<article class="article-body"><div style="background:#fef2f2;border:1px solid #fecaca;border-radius:10px;padding:20px 24px;margin:24px 0;font-size:13px;color:#991b1b;"><p style="font-weight:700;">医疗免责声明</p><p>仅供研究参考用途。在做出任何健康决策前请咨询医疗专业人员。</p></div><div class="quick-answer">{quick_answer_zh}</div>{new_sections}</article>'

    new_html = pattern.sub(lambda _: new_article, html, count=1)
    if new_html == html:
        return False

    zh_path.write_text(new_html, encoding='utf-8')
    return True


def main():
    # File list
    list_path = '/tmp/zh-518-files.txt'
    if not os.path.exists(list_path):
        print(f"Missing {list_path}")
        sys.exit(1)
    files = [l.strip() for l in open(list_path) if l.strip()]

    fixed = skipped = errors = 0
    for fname in files:
        zh_path = ZH_DIR / fname
        en_path = EN_DIR / fname
        if not zh_path.exists():
            skipped += 1
            continue
        try:
            if process_file(zh_path, en_path):
                fixed += 1
            else:
                skipped += 1
        except Exception as e:
            errors += 1
            print(f"ERR {fname}: {e}", file=sys.stderr)

    print(f"\n=== ZH retranslate summary ===")
    print(f"  Fixed:   {fixed}")
    print(f"  Skipped: {skipped}")
    print(f"  Errors:  {errors}")
    print(f"  Total:   {len(files)}")


if __name__ == '__main__':
    main()
