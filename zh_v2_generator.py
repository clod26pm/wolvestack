# Quick answer generator — 134-160 words target for AI citation
# ============================================================
def build_quick_answer(compound_data, slug):
    """Build a 134-160 word Chinese quick answer optimized for AI citation."""
    name = compound_data['zh_name']
    base = compound_data.get('quick_answer_zh') or (compound_data['overview'][:300])
    # Quick answer should ideally be ~ 134-160 words; in Chinese this is ~ 200-280 chars
    # Trim or pad to target
    if len(base) < 200:
        # Pad with mechanism + dosing summary
        addition = f' 机制：{compound_data["mechanism"][:120]}。剂量：{compound_data["dosing"][:80]}。'
        base = base + addition
    if len(base) > 320:
        base = base[:320].rsplit('。', 1)[0] + '。'
    return base


