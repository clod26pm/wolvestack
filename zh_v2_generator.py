# Process zh files
# ============================================================
def process_file(zh_path):
    html = zh_path.read_text(encoding='utf-8')

    m_title = re.search(r'<title>(.*?)</title>', html, re.DOTALL)
    title_zh = m_title.group(1).strip() if m_title else zh_path.stem

    m_desc = re.search(r'<meta\s+content="([^"]*)"\s+name="description"', html)
    if not m_desc:
        m_desc = re.search(r'<meta\s+name="description"\s+content="([^"]*)"', html)
    quick_answer_zh = m_desc.group(1).strip() if m_desc else ''

    slug = zh_path.stem
    new_article, faq_schema = build_article_body(slug, title_zh, quick_answer_zh)

    if new_article is None:
        return False

    # Replace article tag — try <article> first, then <div class="content">
    pattern_article = re.compile(r'(<article[^>]*>)(.*?)(</article>)', re.DOTALL)
    pattern_content = re.compile(r'(<div\s+class="content"[^>]*>)(.*?)(</div>\s*<footer)', re.DOTALL)
    if pattern_article.search(html):
        new_html = pattern_article.sub(lambda _: new_article, html, count=1)
    elif pattern_content.search(html):
        # Wrap our article in a div.content to match template
        wrapped = '<div class="content">' + new_article + '</div>\n<footer'
        new_html = pattern_content.sub(lambda _: wrapped, html, count=1)
    else:
        return False

    # Inject FAQ JSON-LD if not already present
    if faq_schema and 'FAQPage' not in new_html:
        faq_script = f'<script type="application/ld+json">{json.dumps(faq_schema, ensure_ascii=False)}</script>'
        # Insert before </head>
        new_html = new_html.replace('</head>', faq_script + '\n</head>', 1)

    if new_html == html:
        return False

    zh_path.write_text(new_html, encoding='utf-8')
    return True


