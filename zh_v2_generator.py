# Build FAQ schema (JSON-LD) for SERP feature eligibility
# ============================================================
def build_faq_schema(compound_data, paragraphs):
    """Generate JSON-LD FAQPage schema from FAQ-style paragraphs."""
    name = compound_data['zh_name']
    name_en = compound_data.get('en_name', '')
    qa_pairs = []
    for h2, p in paragraphs:
        if h2.startswith('问题') or '?' in h2 or '？' in h2:
            qa_pairs.append((h2, p))
    if len(qa_pairs) < 3:
        # Synthesize FAQ from compound data
        qa_pairs = [
            (f'{name} 是什么？', compound_data['overview'][:400]),
            (f'{name} 的作用机制是什么？', compound_data['mechanism'][:400]),
            (f'{name} 的研究剂量是多少？', compound_data['dosing'][:400]),
            (f'{name} 的主要安全考虑是什么？', compound_data['safety'][:400]),
            (f'{name} 是 FDA 批准的吗？', '该化合物的 FDA 监管状态需要根据具体化合物确认。多数研究化合物未获 FDA 批准用于人体应用，仅作研究用途销售。'),
        ]
    schema = {
        "@context": "https://schema.org",
        "@type": "FAQPage",
        "mainEntity": [
            {
                "@type": "Question",
                "name": q,
                "acceptedAnswer": {"@type": "Answer", "text": a}
            } for q, a in qa_pairs[:6]
        ]
    }
    return schema


