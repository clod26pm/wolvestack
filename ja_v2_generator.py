def process_file(path):
    html = path.read_text(encoding='utf-8')
    m_title = re.search(r'<title>(.*?)</title>', html, re.DOTALL)
    title_ja = m_title.group(1).strip() if m_title else path.stem
    m_desc = re.search(r'<meta\s+content="([^"]*)"\s+name="description"', html)
    if not m_desc:
        m_desc = re.search(r'<meta\s+name="description"\s+content="([^"]*)"', html)
    qa_ja = m_desc.group(1).strip() if m_desc else ''

    slug = path.stem
    new_article, faq = build_article_body(slug, title_ja, qa_ja)
    if new_article is None:
        return False

    pat_a = re.compile(r'(<article[^>]*>)(.*?)(</article>)', re.DOTALL)
    pat_d = re.compile(r'(<div\s+class="content"[^>]*>)(.*?)(</div>\s*<footer)', re.DOTALL)
    if pat_a.search(html):
        new_html = pat_a.sub(lambda _: new_article, html, count=1)
    elif pat_d.search(html):
        wrapped = '<div class="content">' + new_article + '</div>\n<footer'
        new_html = pat_d.sub(lambda _: wrapped, html, count=1)
    else:
        return False

    if faq and 'FAQPage' not in new_html:
        s = f'<script type="application/ld+json">{json.dumps(faq, ensure_ascii=False)}</script>'
        new_html = new_html.replace('</head>', s + '\n</head>', 1)

    if new_html == html:
        return False
    path.write_text(new_html, encoding='utf-8')
    return True


def main():
    list_path = os.environ.get('FILE_LIST', None)
    if list_path and os.path.exists(list_path):
        files = [JA_DIR / l.strip() for l in open(list_path) if l.strip()]
    else:
        files = sorted(JA_DIR.glob('*.html'))
    fixed = skipped = errors = 0
    for path in files:
        if not path.exists():
            skipped += 1
            continue
        try:
            if process_file(path):
                fixed += 1
            else:
                skipped += 1
        except Exception as e:
            errors += 1
            print(f"ERR {path.name}: {e}", file=sys.stderr)
    print(f"\n=== JA v2 summary ===")
    print(f"  Fixed:   {fixed}")
    print(f"  Skipped: {skipped}")
    print(f"  Errors:  {errors}")
    print(f"  Total:   {len(files)}")


if __name__ == '__main__':
    main()
