def parse_slug(slug):
    s = slug.replace('.html', '')
    if s in SKIP_SLUGS:
        return ('__SKIP__', None)
    for prefix, hub_type in HUB_PATTERNS:
        if s.startswith(prefix):
            return (f'__HUB_{hub_type}__', s[len(prefix):])
    compound_keys = sorted(COMPOUNDS.keys(), key=len, reverse=True)
    for ck in compound_keys:
        if s == ck:
            return (ck, 'guide')
        if s.startswith(ck + '-'):
            remainder = s[len(ck) + 1:]
            return (ck, _map_remainder(remainder))
    if '-' in s:
        parts = s.split('-')
        for n in (3, 2, 1):
            if n < len(parts):
                return ('-'.join(parts[:n]), _map_remainder('-'.join(parts[n:])))
    return (s, 'guide')


def _map_remainder(remainder):
    if not remainder:
        return 'guide'
    aspect_map = {
        'guide': 'guide', 'beginners': 'beginners', 'for-beginners': 'beginners',
        'dosage': 'dosing', 'cycle': 'cycling', 'faq': 'faq',
        'side-effects': 'safety', 'safety': 'safety', 'benefits': 'benefits',
        'research': 'research', 'reviews': 'reviews', 'reconstitution': 'reconstitution',
        'storage': 'storage', 'half-life': 'pharmacokinetics',
        'how-it-works': 'mechanism', 'injection-guide': 'injection',
        'where-to-buy': 'sourcing', 'buy': 'sourcing', 'legal': 'legal',
        'stacking': 'stacking', 'results-timeline': 'timeline',
        'timeline': 'timeline', 'before-and-after': 'beforeafter',
        'for-men': 'men', 'for-women': 'women',
    }
    if remainder in aspect_map:
        return aspect_map[remainder]
    if remainder.startswith('vs-') or remainder == 'vs':
        return f'comparison:{remainder[3:] if remainder.startswith("vs-") else "alternatives"}'
    if remainder.startswith('for-'):
        app = remainder[4:]
        if app in APP_TO_ANGLE:
            return APP_TO_ANGLE[app]
        return f'application:{app}'
    if remainder.startswith('and-'):
        return f'interaction:{remainder[4:]}'
    return 'guide'


