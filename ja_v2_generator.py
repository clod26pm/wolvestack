def build_quick_answer(c, slug):
    name = c['ja_name']
    base = c.get('quick_answer_ja') or c['overview'][:300]
    if len(base) < 200:
        addition = f' 機序：{c["mechanism"][:120]}。用量：{c["dosing"][:80]}。'
        base = base + addition
    if len(base) > 320:
        base = base[:320].rsplit('。', 1)[0] + '。'
    return base


def build_faq_schema(c, paragraphs):
    name = c['ja_name']
    qa = []
    for h2, p in paragraphs:
        if '質問' in h2 or '?' in h2 or '？' in h2:
            qa.append((h2, p))
    if len(qa) < 3:
        qa = [
            (f'{name} とは何ですか？', c['overview'][:400]),
            (f'{name} の作用機序は？', c['mechanism'][:400]),
            (f'{name} の研究用量は？', c['dosing'][:400]),
            (f'{name} の主な安全性考慮事項は？', c['safety'][:400]),
            (f'{name} はFDA承認されていますか？', 'この化合物のFDA規制状態は具体的化合物に基づき確認が必要。多くの研究化合物はヒト応用にFDA承認されておらず、研究目的のみで販売される。'),
        ]
    return {
        "@context": "https://schema.org",
        "@type": "FAQPage",
        "mainEntity": [
            {"@type": "Question", "name": q,
             "acceptedAnswer": {"@type": "Answer", "text": a}}
            for q, a in qa[:6]
        ]
    }


